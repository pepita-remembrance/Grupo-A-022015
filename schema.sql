
    create table League (
        id bigint generated by default as identity (start with 1),
        maxTeams integer not null,
        minTeams integer not null,
        name varchar(255),
        primary key (id)
    )

    create table League_Team (
        League_id bigint not null,
        teams_id bigint not null
    )

    create table Match (
        id bigint generated by default as identity (start with 1),
        awayTeam_id bigint,
        homeTeam_id bigint,
        stage_id bigint,
        primary key (id)
    )

    create table Player (
        id bigint generated by default as identity (start with 1),
        name varchar(255),
        position integer,
        realWorldTeam varchar(255),
        team_id bigint,
        primary key (id)
    )

    create table Stage (
        id bigint generated by default as identity (start with 1),
        date date,
        league_id bigint,
        primary key (id)
    )

    create table StageImport (
        id bigint generated by default as identity (start with 1),
        code bigint not null,
        createdAt date,
        primary key (id)
    )

    create table Stage_goals (
        Stage_id bigint not null,
        goals integer,
        player_id bigint not null,
        primary key (Stage_id, player_id)
    )

    create table Team (
        id bigint generated by default as identity (start with 1),
        logoUrl varchar(255),
        name varchar(255),
        primary key (id)
    )

